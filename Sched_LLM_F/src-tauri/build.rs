use std::{fs, path::PathBuf};

fn ensure_icon() {
  let mut icon_path = PathBuf::from(env!("CARGO_MANIFEST_DIR"));
  icon_path.push("icons");
  let _ = fs::create_dir_all(&icon_path);
  icon_path.push("icon.ico");
  let icon_bytes = [
    0x00, 0x00, 0x01, 0x00, 0x01, 0x00, 0x01, 0x01, 0x00, 0x00, 0x01, 0x00, 0x20, 0x00, 0x30,
    0x00, 0x00, 0x00, 0x16, 0x00, 0x00, 0x00, 0x28, 0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00,
    0x02, 0x00, 0x00, 0x00, 0x01, 0x00, 0x20, 0x00, 0x00, 0x00, 0x00, 0x00, 0x08, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00, 0xFF, 0x00, 0x00, 0x00, 0x00, 0x00
  ];
  let needs_write = match fs::read(&icon_path) {
    Ok(existing) => existing != icon_bytes,
    Err(_) => true
  };
  if needs_write {
    let _ = fs::write(icon_path, icon_bytes);
  }
}

fn main() {
  ensure_icon();
  tauri_build::build()
}
